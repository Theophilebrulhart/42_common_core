#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

int main()
{
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("AKA47");
		HumanB jim("Jim");
		jim.setWeapon(&club);
		jim.attack();
		club.setType("MP5");
		jim.attack();
	}
	return 0;
}